public class Funcionarios {

    private double salarioGerente;
    private double salarioSupervisor;
    private double salarioAtendente;
    private double imposto;



    public double getSalarioGerente() {return salarioGerente;}

    public double getSalarioSupervisor() {return salarioSupervisor;}

    public double getSalarioAtendente() {return salarioAtendente;}

    public void setSalarioGerente(double salarioGerente) {this.salarioGerente = salarioGerente;}

    public void setSalarioSupervisor(double salarioSupervisor) {this.salarioSupervisor = salarioSupervisor;}

    public void setSalarioAtendente(double salarioAtendente) {this.salarioAtendente = salarioAtendente;}


    public double getImposto() {
        return imposto;
    }
    public void setImposto(double imposto) {
        this.imposto = imposto;
    }


}
